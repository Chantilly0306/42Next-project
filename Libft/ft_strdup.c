#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*new;

	if (!s)
		return (NULL);
	new = malloc(ft_strlen(s) + 1);
	if (!new)
		return (NULL);
	i = 0;
	while (s[i])
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
